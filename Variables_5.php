<!--

PHP Variables
Variables are "containers" for storing information.

Creating (Declaring) PHP Variables
In PHP, a variable starts with the $ sign, followed by the name of the variable:

Example

After the execution of the statements below, the variable $txt will hold the value Hello world!,
 the variable $x will hold the value 5, and the variable $y will hold the value 10.5.

Note: When you assign a text value to a variable, put quotes around the value.

Note: Unlike other programming languages, PHP has no command for declaring a variable.
 It is created the moment you first assign a value to it.

-->

<?php

$txt = "Hello world!";
$x = 5;
$y = 10.5;

?>